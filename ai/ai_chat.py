import random

STARTERS = [
    "Если бы ты мог прямо сейчас отправиться в любое место мира — куда бы пошёл?",
    "Какой самый необычный комплимент тебе говорили?",
    "Если бы жизнь была фильмом, в каком жанре она была бы?",
    "Что для тебя идеальное свидание?",
    "Представь, что у нас нет интернета. Чем займёмся?"
]

def get_ai_starter():
    """Возвращает случайную фразу для начала разговора"""
    return random.choice(STARTERS)
